package instructions

import (
	"../runtime"
	"../../utils"
	"../../types"
		)

type I_dconstX struct {
}

func init()  {
	INSTRUCTION_MAP[0xe] = &I_dconstX{}
	INSTRUCTION_MAP[0xf] = &I_dconstX{}
}

func (s I_dconstX)Stroke(ctx *runtime.Context) error {
	utils.Log(1, "dconstX exce >>>>>>>>>\n")

	op := ctx.Code[ctx.PC - 1]

	ctx.CurrentFrame.PushFrame(types.Jdouble(uint32(op) - 0xe))
	return nil
}

func (s I_dconstX)Test(octx *runtime.Context) *runtime.Context {
	f := new(runtime.Frame)
	f.PushFrame(&types.Jarray{
		Reference: []types.Jbyte{1, 2, 3, 4},
	})
	f.PushFrame(types.Jdouble(9.123456789012345))
	f.PushFrame(types.Jdouble(9.123456789012343))
	//f.PushFrame(nil)
	a := new(runtime.Aborigines)
	a.Layers = append(a.Layers, &[]uint32{1234})
	return &runtime.Context{
		Code: []byte{0xf},
		CurrentFrame: f,
		CurrentAborigines: a,
	}
}
